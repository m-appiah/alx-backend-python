#!/usr/bin/env python3
"""
This module contains the function to_str that takes a float n as argument
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float number.

    Parameters:
    n (float): The float number to convert to string.

    Returns:
    str: The string representation of the float number.
    """
    return str(n)

#!/usr/bin/env python3
"""
This module contains the function floor that takes a float n as argument
and returns the floor of the float.
"""


def floor(n: float) -> int:
    """
    Returns the floor of a float number.

    Parameters:
    n (float): The float number to floor.

    Returns:
    int: The floor of the float number.
    """
    return int(n // 1)

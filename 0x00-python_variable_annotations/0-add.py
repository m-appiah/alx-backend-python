#!/usr/bin/env python3
"""
This module contains the function add that takes a
float a and a float b as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns their sum as a float.

    Parameters:
    a (float): The first number to be added.
    b (float): The second number to be added.

    Returns:
    float: The sum of the two numbers.
    """
    return a + b
